/*
 * ==========================================
 * 视频源管理功能 - 使用说明文档
 * ==========================================
 * 
 * 功能描述：
 * 该功能允许用户在相机实时画面和导入的视频文件之间灵活切换，
 * 用于测试和验证动作树（工艺流程）的规范性。
 * 
 * ==========================================
 * 组件说明
 * ==========================================
 * 
 * 1. VideoSourceManager 类 (Services/VideoSourceManager.cs)
 *    - 统一管理相机和视频文件两种视频源
 *    - 支持源之间的动态切换
 *    - 实现了循环播放视频文件的功能
 *    
 * 主要方法：
 * - StartCameraAsync(cameraId, cameraIndex)：启动相机源
 * - StartVideoFileAsync(videoFilePath, isLooping)：启动视频文件源
 * - Stop()：停止当前视频源
 * - CurrentSourceType 属性：获取当前源类型（Camera 或 VideoFile）
 * - IsRunning 属性：判断视频源是否正在运行
 * 
 * ==========================================
 * 前端集成说明
 * ==========================================
 * 
 * MainWindow 中新增的功能：
 * 
 * 1. 导入检测视频按钮 (📹 导入检测视频)
 *    - 点击打开文件选择对话框
 *    - 选择支持格式的视频文件 (*.mp4, *.avi, *.mov, *.mkv 等)
 *    - 导入后自动播放并进行动态识别检测
 *    - 视频会循环播放，便于持续检测
 *    调用方法：BtnImportTestVideo_Click()
 * 
 * 2. 启用相机检测按钮 (📷 启用相机检测)
 *    - 点击启用使用工作流配置中的相机进行实时检测
 *    - 相机参数从加载的工作流 JSON 中获取
 *    调用方法：BtnEnableCameraDetection_Click()
 * 
 * 3. 右上角实时检查框 (VideoFeed 控件)
 *    - 实时显示当前选择的视频源（相机或导入的视频文件）
 *    - 同时显示当前工位信息 (📍 工位)
 *    - 显示当前执行步骤 (当前步骤)
 * 
 * ==========================================
 * 使用流程
 * ==========================================
 * 
 * 方案 1：使用相机进行检测
 * =====================================
 * 1. 点击 "📂 导入流程 JSON" 加载工作流配置
 * 2. 等待工作流加载完成（相机会自动启动）
 * 3. 右上角实时检查框会显示相机画面
 * 4. 工艺流程执行器会基于相机帧进行实时识别检测
 * 5. 如果需要重新启动相机，点击 "📷 启用相机检测"
 * 
 * 方案 2：使用视频文件进行检测（测试模式）
 * =====================================
 * 1. 点击 "📂 导入流程 JSON" 加载工作流配置
 * 2. 工作流加载完成后，点击 "📹 导入检测视频"
 * 3. 在文件选择对话框中选择测试视频文件
 * 4. 视频文件会自动开始播放（循环播放）
 * 5. 右上角实时检查框会显示视频内容
 * 6. 工艺流程执行器会基于视频帧进行识别检测
 * 7. 这种方式便于测试复杂工艺流程，无需真实相机和产品
 * 
 * ==========================================
 * 技术实现细节
 * ==========================================
 * 
 * 1. 帧处理管道
 *    - VideoSourceManager 读取每一帧（来自相机或视频文件）
 *    - 将帧转换为 WPF BitmapSource 用于 UI 显示
 *    - 同时将帧传递给 WorkflowExecutor 进行 AI 推理
 * 
 * 2. 循环播放机制（仅限视频文件）
 *    - 当视频文件读取到末尾时，检测 isLooping 标志
 *    - 如果启用循环，重置帧索引到 0，重新开始播放
 *    - 实现连续检测效果，无需用户干预
 * 
 * 3. 错误处理
 *    - 相机打开失败时，显示人性化错误提示
 *    - 视频文件不存在或格式不支持时，也会显示提示
 *    - 所有错误都会记录到系统日志中
 * 
 * 4. 资源清理
 *    - 窗口关闭时自动释放 VideoSourceManager 资源
 *    - 停止相机/视频读取线程
 *    - 释放 OpenCV VideoCapture 对象
 * 
 * ==========================================
 * 兼容性
 * ==========================================
 * 
 * - 支持的视频格式：MP4, AVI, MOV, MKV 等（取决于 OpenCV 编译时支持的编码器）
 * - 支持的相机：DirectShow 兼容的 USB 摄像头
 * - 依赖库：OpenCvSharp 4.x（已集成到项目中）
 * 
 * ==========================================
 * 常见问题 (FAQ)
 * ==========================================
 * 
 * Q1: 导入视频后，右上角看不到画面？
 * A1: 
 *   - 检查视频文件是否完整且格式正确
 *   - 查看系统日志是否有错误信息
 *   - 尝试用其他视频播放器测试该文件
 * 
 * Q2: 相机无法启动？
 * A2:
 *   - 检查相机是否已连接并被操作系统识别
 *   - 确保没有其他应用占用相机
 *   - 查看工作流 JSON 中的相机配置是否正确
 * 
 * Q3: 视频播放很卡顿？
 * A3:
 *   - 可能是视频分辨率过高或格式不优化
 *   - 尝试用编码工具重新压缩视频（降低分辨率和比特率）
 *   - 检查系统资源占用情况
 * 
 * Q4: 如何在相机和视频之间快速切换？
 * A4:
 *   - 使用 "📷 启用相机检测" 切换到相机源
 *   - 使用 "📹 导入检测视频" 切换到视频源
 *   - 会自动停止当前源，启动新源
 * 
 * ==========================================
 * 后续扩展建议
 * ==========================================
 * 
 * 1. 支持多相机同时监控
 * 2. 添加视频源参数调节面板（分辨率、帧率等）
 * 3. 支持录制检测过程的视频
 * 4. 添加视频文件列表管理（快速切换）
 * 5. 支持音频同步检测
 */
