 * 【核心算法说明】
 * ===============================================================================
 * 
 * Letterbox算法流程：
 * 
 * 输入: 任意尺寸图像（如1920x1080）
 * ↓
 * 1. 计算等比例缩放因子
 *    scale = min(640/1920, 640/1080) = 0.333
 * ↓
 * 2. 缩放图像
 *    640 × 0.333 = 213 (新宽度)
 *    480 × 0.333 = 160 (新高度)
 *    结果：213×160
 * ↓
 * 3. 创建640×640白布（灰色）
 * ↓
 * 4. 将缩放后的图像贴在中间
 *    └─ 上下各填充 (640-160)/2 = 240 像素
 *    └─ 左右各填充 (640-213)/2 = 214 像素
 * ↓
 * 输出：640×640标准尺寸图像（无变形）
 * 
 * ===============================================================================
